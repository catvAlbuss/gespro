export function getPixelCoord(value, values) {
  const axes = Object.keys(values)
    .map((axis) => parseFloat(axis))
    .sort((a, b) => a - b);
  const top = axes.findIndex((axis) => {
    return axis >= value;
  });
  if (top > 0) {
    const low = axes[top - 1];
    const high = axes[top];
    return map(value, low, high, values[low], values[high]);
  } else if (value <= axes[0]) {
    return values[axes[0]];
  } else {
    return values[axes[axes.length - 1]];
  }
}
